package Entities;

/**
 *
 * @author Carlos SMY
 */
public class TypeNote {
    int id;
    String type;

    public TypeNote(int id, String type) {
        this.id = id;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setType(String type) {
        this.type = type;
    }
    
    
    
}
